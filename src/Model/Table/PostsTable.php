<?php
namespace App\Model\Table;

use App\Model\Entity\Post;
use Cake\ORM\Query;
use Cake\ORM\RulesChecker;
use Cake\ORM\Table;
use Cake\Validation\Validator;

/**
 * Posts Model
 *
 * @property \Cake\ORM\Association\BelongsTo $Modeuses
 */
class PostsTable extends Table
{

    /**
     * Initialize method
     *
     * @param array $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config)
    {
        $this->table('posts');
        $this->displayField('title');
        $this->primaryKey('id');
        $this->belongsTo('Modeuses', [
            'foreignKey' => 'modeuse_id',
            'joinType' => 'INNER'
        ]);
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator)
    {
        $validator
            ->add('id', 'valid', ['rule' => 'numeric'])
            ->allowEmpty('id', 'create');
            
        $validator
            ->requirePresence('social', 'create')
            ->notEmpty('social');
            
        $validator
            ->allowEmpty('title');
            
        $validator
            ->allowEmpty('content');
            
        $validator
            ->allowEmpty('picture');
            
        $validator
            ->add('likes', 'valid', ['rule' => 'numeric'])
            ->allowEmpty('likes');
            
        $validator
            ->add('number', 'valid', ['rule' => 'numeric'])
            ->requirePresence('number', 'create')
            ->notEmpty('number');
            
        $validator
            ->add('shares', 'valid', ['rule' => 'numeric'])
            ->allowEmpty('shares');
            
        $validator
            ->add('comments', 'valid', ['rule' => 'numeric'])
            ->allowEmpty('comments');
            
        $validator
            ->add('nb_tweets', 'valid', ['rule' => 'numeric'])
            ->allowEmpty('nb_tweets');

        return $validator;
    }

    /**
     * Returns a rules checker object that will be used for validating
     * application integrity.
     *
     * @param \Cake\ORM\RulesChecker $rules The rules object to be modified.
     * @return \Cake\ORM\RulesChecker
     */
    public function buildRules(RulesChecker $rules)
    {
        $rules->add($rules->existsIn(['modeuse_id'], 'Modeuses'));
        return $rules;
    }
}
